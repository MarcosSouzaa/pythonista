# Por que queremos comparar?

print('Comaração com tipos lógicos')
idade = 15
# Compara um valor com outro e traz uma resposta
print(idade > 18) # False
print(idade < 18) # True
print(idade <= 18) # True
print(idade >= 18) # False
print(idade == 18) # False
print(idade != 18) # True

